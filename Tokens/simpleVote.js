window.addEventListener('load', function() {

  //Checking if Web3 has been injected by the browser (Mist/MetaMask)
  if (typeof web3 !== 'undefined') {
    // Use Mist/MetaMask's provider
    window.web3 = new Web3(web3.currentProvider);
  } else {
    console.log('No web3? You should consider trying MetaMask!')
    // fallback - use your fallback strategy (local node / hosted node + in-dapp id mgmt / fail)
    window.web3 = new Web3(new Web3.providers.HttpProvider("http://166.104.144.105:8500"));
	}

  // Now you can start your app & access web3 freely:
  startApp();
});

function startApp() {
  tokenControllerContract = web3.eth.contract(controllerAbi); // controller
  tokenController = tokenControllerContract.at(controllerAddr);
  document.getElementById('controllerAddr').innerHTML = getLink(controllerAddr);

  butterContract = web3.eth.contract(butterAbi); // controller
  butterToken = butterContract.at(butterTokenAddr);
  document.getElementById('butterAddr').innerHTML = getLink(butterTokenAddr);

  bakingMasteryContract = web3.eth.contract(bakingMasteryAbi); // controller
  bakingMastery = bakingMasteryContract.at(bakingMasteryAddr);
  document.getElementById('bakingMasteryAddr').innerHTML = getLink(bakingMasteryAddr);

  web3.eth.getAccounts(function(e,r){
  document.getElementById('accountAddr').innerHTML = getLink(r[0]);
  accountAddress = r[0];
  getValue();
  });
}

function getLink(addr) {
  return '<a target="_blank" href=https://rinkeby.etherscan.io/address/' + addr + '>' + addr +'</a>';
}

function getValue() {
  getEther();
  getToken();
  getTokenInfo();
  getCandidateInfo();
}

function getEther() {
  web3.eth.getBalance(accountAddress, function(e,r){
    document.getElementById('ethValue').innerHTML =web3.fromWei(r.toString()) + "ETH";
  });
}

function getToken() {
  butterToken.getBalanceOf(function(e,r){
    document.getElementById('butterTokenValue').innerHTML = r.toString();
  });

  bakingMastery.getBalanceOf(function(e,r){
    document.getElementById('bakingTokenValue').innerHTML = r.toString();
  });
}

function getTokenInfo() {

  butterToken.getInitialSupply(function(e,r){
    document.getElementById('butter-tokens-total').innerHTML = r.toString();
  });
  butterToken.getTotalSupply(function(e,r){
    document.getElementById('butter-tokens-sellable').innerHTML = r.toString();
  });
  butterToken.getTokenPrice(function(e,r){
    tokenPrice = parseFloat(web3.fromWei(r.toString()));
    document.getElementById('butter-token-cost').innerHTML = tokenPrice + "ETH";
  });
  web3.eth.getBalance(butterToken.address, function(e,v) {
    document.getElementById('butter-contract-balance').innerHTML = web3.fromWei(v.toString()) + "ETH";
  });

  bakingMastery.totalSupply(function(e,r){
    document.getElementById('baking-tokens-total').innerHTML = r.toString();
  });
  bakingMastery.totalSupply(function(e,r){
    document.getElementById('baking-tokens-sellable').innerHTML = r.toString();
  });
}


function buyTokens() {
  let tokensToBuy = $("#buy").val();
  let price = tokensToBuy * tokenPrice;
  $("#buy-msg").html("Purchase order has been submitted. Please wait.");

  butterToken.buyToken({value: web3.toWei(price, 'ether'), from: web3.eth.accounts[0]}, function(v) {
    web3.eth.getBalance(butterToken.address, function(e, r) {
    $("#contract-balance").html(web3.fromWei(r.toString()) + " ETH");
   });
  });
}


function switchTokens() {
  let tokensToSwitch = $("#switch").val();
  let price = tokensToSwitch;
  $("#switch-msg").html("Switch order has been submitted. Please wait.");

  tokenController.convertTokens({value: price, from: web3.eth.accounts[0]});
}
